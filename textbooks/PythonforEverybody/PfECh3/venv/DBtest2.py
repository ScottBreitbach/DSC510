import tkinter
def kmph_to_mph():
    kmph = float(kmph_entry.get())
    mph = float(kmph*0.62)
    mph_label.config(text= round(mph,2))
window = tkinter.Tk()
window.title("kmph to mph converter")
window.geometry('200x150')
kmph_label = tkinter.Label(text="kmph: ")
kmph_entry = tkinter.Entry()
mph_label = tkinter.Label(text="")
unit_label = tkinter.Label(text="mph: ")
convert_button = tkinter.Button(text='Convert', command=kmph_to_mph)
kmph_label.grid(column=0, row=0)
kmph_entry.grid(column=1, row=0)
mph_label.grid(column=1, row=1)
unit_label.grid(column=0, row=1)
convert_button.grid(column=1, row=3)
window.mainloop()
